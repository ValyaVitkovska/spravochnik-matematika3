// Универсална таблица: { title, headers: [...], rows: [[...], ...] }
function makeCustomTable(t) {
  const head = '<tr>' + t.headers.map(h => `<th>${h}</th>`).join('') + '</tr>';
  const body = t.rows.map(row =>
    '<tr>' + row.map(cell => `<td>${cell}</td>`).join('') + '</tr>'
  ).join('');
  return `<table class="data-table"><thead>${head}</thead><tbody>${body}</tbody></table>`;
}

